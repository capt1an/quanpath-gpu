#define HANDLE_CUDA_ERROR(call) \
    ([](cudaError_t err) { \
        if (err != cudaSuccess) { \
            fprintf(stderr, "CUDA Error at %s:%d: %s\n", __FILE__, __LINE__, cudaGetErrorString(err)); \
        } \
        return err; \
    })((call))
